/**
 * UC1_ApplicationEntry
 *
 * This class represents the entry point of the Hotel Booking Management System.
 * It demonstrates how a Java application begins execution and prints
 * a welcome message to the console.
 *
 * @author Nidhi
 * @version 1.0
 */

public class UC1_ApplicationEntry {

    /**
     * Main method – entry point of the Java application
     */
    public static void main(String[] args) {

        // Welcome message
        System.out.println("=======================================");
        System.out.println("   Welcome to Hotel Booking System");
        System.out.println("   Version: 1.0");
        System.out.println("=======================================");

        // Application startup confirmation
        System.out.println("Application started successfully.");

        // Application termination
        System.out.println("Application terminated.");
    }
}